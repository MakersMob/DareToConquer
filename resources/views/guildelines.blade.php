                <li><strong>Be encouraging</strong>. Starting your own business can be hard, scary, lonely and frustrating. And awesome, but at first it feels like a lot of those first four things. It’s hard to find someone in real life who really gets what you’re doing with this blogging business. Slack is a great place to be around people that are going through, or have gone through, the same things you are going through. So support and encourage each other. If you’re having a rough day, pop in to Slack and say hi. Ask for help. Make someone tell you a joke. It’s the fastest way to turn a bad day around.</li>
                <li><strong>Be helpful</strong>. This whole thing falls apart if we aren’t helping each other. I don’t know everything, Scrivs doesn’t know everything (shh, don’t tell him I said that) but all of us together know nearly everything. If you see a question you can answer, answer it! If you find some awesome thing that is helping you with your business, share it!</li>
            </ul>
            <p>Finally, a few tips to keep Slack the enjoyable, helpful place that you have all made it:</p>
            <ul>
                <li>Please don’t share affiliate links in Slack. If you’ve got one and you know someone who might be interested, you can DM them.</li>
                <li>Please keep discussions about other blogging courses that cover the same topics as DTC out of the public channels.</li>
